public class Loops {
    public static void main(String[] args) {
       

        // print 1-5
        for (int i = 0; i<6; i++){
            System.out.println("for");
        }

            // while loop


            int i =0;
            while(i<6){
                System.out.println("while");
                i++;
            }


            // do-while

            do{
                System.out.println("do-while");
                i++;
            }
            while(i<0);

        
    }
    
}
